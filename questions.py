def get_all_questions():
    """Возвращает все вопросы, перемешанные"""
    all_questions = []
    question_id = 1
    
    # Добавляем вопросы из всех предметов
    for subject, questions in GENERAL_TEST_QUESTIONS.items():
        for q in questions:
            all_questions.append({
                'id': question_id,
                'subject': subject,
                'question': q['question'],
                'options': q['options'],
                'correct': q['correct']
            })
            question_id += 1
    
    return all_questions

def get_question_by_id(questions, q_id):
    """Получить вопрос по ID"""
    for q in questions:
        if q['id'] == q_id:
            return q
    return None
